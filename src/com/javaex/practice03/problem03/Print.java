package com.javaex.practice03.problem03;

public class Print 
{	
	public Print(int i)
	{
		System.out.println(i);
	}
	
	public Print(boolean b)
	{
		System.out.println(b);
	}
	
	public Print(float f)
	{
		System.out.println(f);
	}
	
	public Print(String s)
	{
		System.out.println(s);
	}
}
